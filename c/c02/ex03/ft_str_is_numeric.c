int	ft_str_is_numeric(char *str)
{
	int i;
	int x;

	x = 1;
	i = 0;
	while (str[i] != 0 && x == 1)
	{
		if (str[i] < '0' || str[i] > '9')
			x = 0;
		i++;
	}
	return (x);
}
