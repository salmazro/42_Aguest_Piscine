int	ft_str_is_lowercase(char *str)
{
	int i;
	int x;

	x = 1;
	i = 0;
	while (str[i] != 0 && x == 1)
	{
		if (str[i] < 'a' || str[i] > 'z')
			x = 0;
		i++;
	}
	return (x);
}
