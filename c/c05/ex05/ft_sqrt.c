int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0)
		return (0);
	else
	{
		while (i <= nb / 2 || i == nb)
		{
			if (i > 46341)
				return (0);
			if (i * i == nb)
				return (i);
			else
				i++;
		}
	}
	return (0);
}
